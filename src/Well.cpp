static float norm(uint8_t b) {
    return (float)((signed char)(b - 0x80)) / 112.0f;
}

void Well::Init(int waveNum) {
    int wellIdx = WELL_SEQUENCE[ (waveNum - 1) % 16 ];
    const WellRawData& raw = WELL_DATA_TABLE[wellIdx];

    def_.isClosed = (raw.isClosed == 0);
    def_.eyeY = (float)((signed char)(raw.eyeY - 0x18)); // HOLEYL 0x18 = 0 bias?
    def_.eyeZ = (float)((signed char)(raw.eyeZ - 0x50)); // HOLEZL 0x50 = 0 bias?
    
    for (int i = 0; i < NUM_LANES; ++i) {
        def_.rimAngles[i] = norm(raw.xAngles[i]);
        def_.depAngles[i] = norm(raw.zAngles[i]);
    }
    
    numLanes_ = NUM_LANES;
    BuildRim_();
}

void Well::BuildRim_() {
    float cx = SCREEN_W * 0.5f;
    float cy = SCREEN_H * 0.5f;
    float rimRadius = SCREEN_H * 0.42f;

    rimVertices_.assign(numLanes_, {0,0});
    for (int i = 0; i < numLanes_; ++i) {
        rimVertices_[i].x = cx + def_.rimAngles[i] * rimRadius;
        rimVertices_[i].y = cy + def_.depAngles[i] * rimRadius;
    }
    // Vanishing point using eye offset
    vanishingPoint_ = { cx + def_.eyeY * 10.0f,
                        cy + def_.eyeZ * 10.0f };
}

Vector2 Well::Project(WellPoint wp) const {
    float lane = wp.lane;
    if (def_.isClosed) {
        lane = fmodf(lane, (float)numLanes_);
        if (lane < 0.0f) {
            lane += (float)numLanes_;
        }
    } else {
        lane = std::clamp(lane, 0.0f, (float)(numLanes_ - 1));
    }

    const float laneFloor = floorf(lane);
    int i0 = (int)laneFloor;
    if (i0 < 0) {
        i0 = 0;
    }
    if (i0 >= numLanes_) {
        i0 = numLanes_ - 1;
    }

    int i1 = def_.isClosed ? ((i0 + 1) % numLanes_) : std::min(i0 + 1, numLanes_ - 1);
    float t_lane = lane - laneFloor;
    if (!def_.isClosed && i0 == i1) {
        t_lane = 0.0f;
    }
    
    Vector2 v0 = rimVertices_[i0];
    Vector2 v1 = rimVertices_[i1];
    Vector2 rimPos = {
        v0.x + (v1.x - v0.x) * t_lane,
        v0.y + (v1.y - v0.y) * t_lane
    };
    
    float t = wp.depth;
    Vector2 result;
    result.x = vanishingPoint_.x + (rimPos.x - vanishingPoint_.x) * (1.0f - t);
    result.y = vanishingPoint_.y + (rimPos.y - vanishingPoint_.y) * (1.0f - t);
    return result;
}

float Well::GetRimAngle(float lane) const {
    Vector2 p = Project({lane, 0.0f});
    return atan2f(p.y - vanishingPoint_.y, p.x - vanishingPoint_.x);
}
